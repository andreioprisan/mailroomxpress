    <h2>What if I need a larger plan?</h2>
    <p>
      Contact us at <a href="mailto:support@github.com">support@github.com</a>
      to work something out. We also offer an installable version called
      <a href="http://fi.github.com">GitHub:FI</a> for enterprise customers.
    </p>

    <h2>What&rsquo;s different about an organization account?</h2>
    <p>
      A GitHub organization is a profile on GitHub that is managed from your
      personal account.  Organizations are best suited for businesses who need
      to manage permissions for many employees.
    </p>

    <h2>Why don&rsquo;t I see any disk space limits?</h2>
    <p>
      GitHub does not have any hard storage limits.  We have soft limits for
      each plan to prevent abuse.  We find almost everyone uses a small fraction
      of this limit.  If you find yourself needing more disk space for a
      legitimate use, email <a href="mailto:support@github.com">support@github.com</a>.
    </p>

    <h2>What is a private collaborator?</h2>
    <p>
      Private collaborators are GitHub users that you&rsquo;ve given
      access to one or more of your private repositories.
    </p>
    <p>
      Private collaborators may fork any private repository you&rsquo;ve
      added them to without their own paid plan. Their forks do not count
      against your private repository quota.
    </p>

